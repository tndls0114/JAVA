package java4;

public class Op {

	public static void main(String[] args) {
		
		int result;
		double resultDouble;
		
		result = 3+2;  
		resultDouble=3+2;
		System.out.println(result);  //5
		System.out.println(resultDouble); //5
		
		result = 3-2;
		resultDouble=3-2;
		System.out.println(result); //1
		System.out.println(resultDouble);//1
		
		result = 3*2;  
		resultDouble=3*2;  
		System.out.println(result); //6
		System.out.println(resultDouble);  //6
		result = 3/2; 
		resultDouble=3/2;
		System.out.println(result); //1
		System.out.println(resultDouble); //1
		resultDouble=3.0/2.0;
		System.out.println(resultDouble);  //1.5
		result=3%2;
		System.out.println(result); //1
		
		resultDouble=3.0/2;
		System.out.println(resultDouble);
		resultDouble=3/2.0;
		System.out.println(resultDouble);
		
		
		
		
	}

}
